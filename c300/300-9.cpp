/* prog3_4, 以ASCII碼設定字元 */
#include <stdio.h>
#include <stdlib.h>
int main(void)
{
   printf("恩師好!我是學生張洪鈞\n");   	/* 印出Hello 偉大的恩師! 字串 */
   printf("我的C300-9\n");        /* 印出Hello Taiwan! 字串 */ 
   char ch=90;				/* 將整數90設給字元變數ch */
   printf("ch=%c\n",ch);		/* 印出ch的值 */
   
   system("pause");
   return 0;
}


